the file having shell commands is created with the extension ".sh" or ".bash"

sh stands for "shell"
bash stands for "born again shell"

both file can be executed using the following syntaes
1) ./file_name      2)bash file_name

to see the types of shells in your OS enter "cat /etc/shells"
for example:
┌──(root㉿haider)-[~]
└─# cat /etc/shells
# /etc/shells: valid login shells
/bin/sh
/bin/bash
/usr/bin/bash
/bin/rbash
/usr/bin/rbash
/bin/dash
/usr/bin/dash
/usr/bin/pwsh
/opt/microsoft/powershell/7/pwsh
/usr/bin/tmux
/usr/bin/screen
/bin/zsh
/usr/bin/zsh
/usr/bin/sh
 
to know where an 'x' shell is located enter "which x"
for example:
┌──(root㉿haider)-[~]
└─# which bash
/usr/bin/bash


