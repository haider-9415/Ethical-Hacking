conditional statements


these are three types

type-1 --> if-fi statement
syntax:
        if [ conditon ] 
        then
        	statement
        fi

type-2 --> if-else-fi statement
synatx:
       if [ conditon ]
       then
       		statement
       else
		statement
       fi

type-3 --> if-elif-fi statement
syntax:
       if [ conditon ]
       then
       		statement
       elif-1 [ conditon ]
       then 	
		statement
       elif-2 [ conditon ]
       then 	
		statement
             .
             .
             .
       elif-n [ conditon ]
       then 	
	       		statement
       else
       		statement
       fi